
namespace YooAsset
{
    public enum EFileNameStyle
    {
        /// <summary>
        /// 哈希值名称
        /// </summary>
        HashName = 0,

        /// <summary>
        /// 资源包名称（不推荐）
        /// </summary>
        BundleName = 1,

        /// <summary>
        /// 资源包名称 + 哈希值名称
        /// </summary>
        BundleName_HashName = 2,

        //? 为什么“只用资源包名”不推荐？
        //!? 同一个包名，内容经常会变：你改了贴图、压缩设置、加密方式，生成出来的包虽然“名字没变”，但“字节内容变了”。
        //!? CDN/浏览器/本地缓存看的是“文件名”：名字不变，老文件可能被长期缓存，新版本的客户端会拿到旧内容，出现“更新不生效/加载错资源”的问题。
        //!? 运行时也按“最终文件名”建索引：清单里有一个用 FileName 做键的字典。不同版本如果文件名一样，就难以在同一机器上同时存在两份不同内容（老版本/新版本无法并存）。
        //!? 文件名里带内容哈希，相当于“内容身份证”。内容一变，文件名必变，天然解决缓存问题（所谓 cache busting）。
        //!? 多版本可以并存：老版本指向旧哈希，新版本指向新哈希，两者的文件名不同，不会互相覆盖。

        //? 如果使用了HashName，要如何确认是与哪个BundleName对应？
        
    }
}